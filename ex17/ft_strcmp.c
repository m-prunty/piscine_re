int ft_strcmp(char *s1, char *s2)
{
	while (s1[0] || s2[0])
	{
		if (s1[0] - s2[0])
			return (*s1 - *s2);
		s1++;
		s2++;
	}
}

int main()
{
	printf("%i",ft_strcmp("abd","abc"));
	printf("%i",ft_strcmp("abc","abc"));
	printf("%i",ft_strcmp("ab","abc"));

	}
